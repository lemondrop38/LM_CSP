# LM 6th Silly Sentences

noun = input("Give me a noun:").title().strip()
verb = input("Give me a past tense verb:").lower().strip()
onomatopeia = input("Give me a onomatopia:").upper().strip()

print(noun, "and I were so exited as we", verb, "through the forest, until we heard a", onomatopeia, "in the distance.")
